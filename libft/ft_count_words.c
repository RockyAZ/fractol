#include "libft.h"

int	ft_count_words(char *str)
{
	int		res;
	char	*cp;

	res = 0;
	cp = str;
	while (*cp != '\0')
	{
		while (*cp == ' ' || *cp == '\t')
			cp++;
		if (*cp != '\0')
		{
			res++;
			while (*cp != ' ' && *cp != '\t' && *cp != '\0')
				cp++;
		}
	}
	return (res);
}
